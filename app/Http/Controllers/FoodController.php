     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $food = new Food();

        $food->id_user = auth()->user()->id;
        $food->superMarket = $request->input('superMarket');
        $food->market = $request->input('market');
        $food->other = $request->input('other');
        $food->total = array_sum($request->all());

        $food->save();
        return redirect('saving/create');
    }

